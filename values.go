package main

import "fmt" 	//Package fmt implements formatted I/O with functions analogous to C's printf and scanf.

func main() {
	
	fmt.Println("go" + "lang")
	fmt.Println("1+1 =", 1+1)
	fmt.Println("7.0/3.0", 7.0/3.0)

	fmt.Println(true && false)
	fmt.Println(true || false)
	fmt.Println(!true)
	
}
